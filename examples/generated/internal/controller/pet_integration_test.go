package controller

import (
	"time"

	. "github.com/onsi/ginkgo/v2"
	. "github.com/onsi/gomega"
	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"
	"k8s.io/apimachinery/pkg/types"

	v1alpha1 "github.com/bluecontainer/petstore-operator/api/v1alpha1"
)

var _ = Describe("Pet Controller Integration", func() {
	const (
		timeout  = time.Second * 10
		interval = time.Millisecond * 250

		resourceName      = "test-pet"
		resourceNamespace = "default"
	)

	Context("When working with Pet resources", func() {
		AfterEach(func() {
			// Clean up the resource after each test
			pet := &v1alpha1.Pet{}
			err := GetK8sClient().Get(GetContext(), types.NamespacedName{
				Name:      resourceName,
				Namespace: resourceNamespace,
			}, pet)
			if err == nil {
				// Delete the resource
				_ = GetK8sClient().Delete(GetContext(), pet)
				// Wait for deletion
				Eventually(func() bool {
					err := GetK8sClient().Get(GetContext(), types.NamespacedName{
						Name:      resourceName,
						Namespace: resourceNamespace,
					}, &v1alpha1.Pet{})
					return err != nil
				}, timeout, interval).Should(BeTrue())
			}
		})

		It("Should create a Pet resource successfully", func() {
			By("Creating a new Pet")
			pet := &v1alpha1.Pet{
				TypeMeta: metav1.TypeMeta{
					APIVersion: "petstore.example.com/v1alpha1",
					Kind:       "Pet",
				},
				ObjectMeta: metav1.ObjectMeta{
					Name:      resourceName,
					Namespace: resourceNamespace,
				},
				Spec: v1alpha1.PetSpec{},
			}
			Expect(GetK8sClient().Create(GetContext(), pet)).To(Succeed())

			By("Verifying the resource was created")
			created := &v1alpha1.Pet{}
			Eventually(func() error {
				return GetK8sClient().Get(GetContext(), types.NamespacedName{
					Name:      resourceName,
					Namespace: resourceNamespace,
				}, created)
			}, timeout, interval).Should(Succeed())

			Expect(created.Name).To(Equal(resourceName))
			Expect(created.Namespace).To(Equal(resourceNamespace))
		})

		It("Should update a Pet resource", func() {
			By("Creating a new Pet")
			pet := &v1alpha1.Pet{
				ObjectMeta: metav1.ObjectMeta{
					Name:      resourceName,
					Namespace: resourceNamespace,
				},
				Spec: v1alpha1.PetSpec{},
			}
			Expect(GetK8sClient().Create(GetContext(), pet)).To(Succeed())

			By("Updating the Pet")
			updated := &v1alpha1.Pet{}
			Eventually(func() error {
				return GetK8sClient().Get(GetContext(), types.NamespacedName{
					Name:      resourceName,
					Namespace: resourceNamespace,
				}, updated)
			}, timeout, interval).Should(Succeed())

			// Update a field
			if updated.Spec.Target == nil {
				updated.Spec.Target = &v1alpha1.TargetSpec{}
			}
			updated.Spec.Target.Namespace = "updated-namespace"
			Expect(GetK8sClient().Update(GetContext(), updated)).To(Succeed())

			By("Verifying the update")
			Eventually(func() string {
				fetched := &v1alpha1.Pet{}
				_ = GetK8sClient().Get(GetContext(), types.NamespacedName{
					Name:      resourceName,
					Namespace: resourceNamespace,
				}, fetched)
				if fetched.Spec.Target == nil {
					return ""
				}
				return fetched.Spec.Target.Namespace
			}, timeout, interval).Should(Equal("updated-namespace"))
		})

		It("Should delete a Pet resource", func() {
			By("Creating a new Pet")
			pet := &v1alpha1.Pet{
				ObjectMeta: metav1.ObjectMeta{
					Name:      resourceName,
					Namespace: resourceNamespace,
				},
				Spec: v1alpha1.PetSpec{},
			}
			Expect(GetK8sClient().Create(GetContext(), pet)).To(Succeed())

			By("Deleting the Pet")
			toDelete := &v1alpha1.Pet{}
			Expect(GetK8sClient().Get(GetContext(), types.NamespacedName{
				Name:      resourceName,
				Namespace: resourceNamespace,
			}, toDelete)).To(Succeed())
			Expect(GetK8sClient().Delete(GetContext(), toDelete)).To(Succeed())

			By("Verifying the resource is deleted")
			Eventually(func() bool {
				err := GetK8sClient().Get(GetContext(), types.NamespacedName{
					Name:      resourceName,
					Namespace: resourceNamespace,
				}, &v1alpha1.Pet{})
				return err != nil
			}, timeout, interval).Should(BeTrue())
		})
	})
})
