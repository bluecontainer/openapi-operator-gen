// evaluateDerivedValues evaluates CEL expressions against the aggregated resources
// celResources contains full resource data with spec, status, and metadata for each CR
// celDataByKind contains kind-specific lists (e.g., "orders", "pets", "users")
func (r *PetstoreAggregateReconciler) evaluateDerivedValues(ctx context.Context, instance *v1alpha1.PetstoreAggregate, celResources []map[string]interface{}, celDataByKind map[string][]map[string]interface{}) []v1alpha1.ComputedValue {
	logger := log.FromContext(ctx)
	var results []v1alpha1.ComputedValue

	// Create CEL environment with custom types and aggregate functions
	envOpts := []cel.EnvOption{
		cel.Variable("resources", cel.ListType(cel.MapType(cel.StringType, cel.DynType))),
		cel.Variable("summary", cel.MapType(cel.StringType, cel.IntType)),
		// Kind-specific variables (e.g., orders, pets, users)
		cel.Variable("orders", cel.ListType(cel.MapType(cel.StringType, cel.DynType))),
		cel.Variable("pets", cel.ListType(cel.MapType(cel.StringType, cel.DynType))),
		cel.Variable("users", cel.ListType(cel.MapType(cel.StringType, cel.DynType))),
		// Custom aggregate functions for numeric lists
		cel.Function("sum",
			cel.Overload("sum_list",
				[]*cel.Type{cel.ListType(cel.DynType)},
				cel.DoubleType,
				cel.UnaryBinding(celSumList),
			),
		),
		cel.Function("max",
			cel.Overload("max_list",
				[]*cel.Type{cel.ListType(cel.DynType)},
				cel.DoubleType,
				cel.UnaryBinding(celMaxList),
			),
		),
		cel.Function("min",
			cel.Overload("min_list",
				[]*cel.Type{cel.ListType(cel.DynType)},
				cel.DoubleType,
				cel.UnaryBinding(celMinList),
			),
		),
		cel.Function("avg",
			cel.Overload("avg_list",
				[]*cel.Type{cel.ListType(cel.DynType)},
				cel.DoubleType,
				cel.UnaryBinding(celAvgList),
			),
		),
	}

	env, err := cel.NewEnv(envOpts...)
	if err != nil {
		logger.Error(err, "Failed to create CEL environment")
		for _, dv := range instance.Spec.DerivedValues {
			results = append(results, v1alpha1.ComputedValue{
				Name:  dv.Name,
				Error: fmt.Sprintf("failed to create CEL environment: %v", err),
			})
		}
		return results
	}

	// Convert summary to CEL-compatible format
	celSummary := map[string]int64{
		"total":   int64(instance.Status.Summary.Total),
		"synced":  int64(instance.Status.Summary.Synced),
		"failed":  int64(instance.Status.Summary.Failed),
		"pending": int64(instance.Status.Summary.Pending),
	}

	// Build evaluation variables map with all resources and kind-specific lists
	evalVars := map[string]interface{}{
		"resources": celResources,
		"summary":   celSummary,
	}
	// Add kind-specific lists
	for kindKey, kindData := range celDataByKind {
		evalVars[kindKey] = kindData
	}

	// Evaluate each derived value expression
	for _, dv := range instance.Spec.DerivedValues {
		result := v1alpha1.ComputedValue{Name: dv.Name}

		// Parse and check the expression
		ast, issues := env.Compile(dv.Expression)
		if issues != nil && issues.Err() != nil {
			result.Error = fmt.Sprintf("compile error: %v", issues.Err())
			results = append(results, result)
			continue
		}

		// Create program
		prg, err := env.Program(ast)
		if err != nil {
			result.Error = fmt.Sprintf("program error: %v", err)
			results = append(results, result)
			continue
		}

		// Evaluate the expression
		out, _, err := prg.Eval(evalVars)
		if err != nil {
			result.Error = fmt.Sprintf("eval error: %v", err)
			results = append(results, result)
			continue
		}

		// Convert result to string
		result.Value = celValueToString(out)
		results = append(results, result)

		logger.V(1).Info("Evaluated CEL expression",
			"name", dv.Name,
			"expression", dv.Expression,
			"result", result.Value)
	}

	return results
}

// celValueToString converts a CEL value to a string representation
func celValueToString(val ref.Val) string {
	switch v := val.(type) {
	case types.Int:
		return fmt.Sprintf("%d", int64(v))
	case types.Double:
		return fmt.Sprintf("%g", float64(v))
	case types.Bool:
		return fmt.Sprintf("%t", bool(v))
	case types.String:
		return string(v)
	default:
		// For complex types, use reflection or native value
		native := val.Value()
		if native == nil {
			return "null"
		}
		rv := reflect.ValueOf(native)
		switch rv.Kind() {
		case reflect.Slice, reflect.Array:
			return fmt.Sprintf("[%d items]", rv.Len())
		case reflect.Map:
			return fmt.Sprintf("{%d entries}", rv.Len())
		default:
			return fmt.Sprintf("%v", native)
		}
	}
}

