package controller

import (
	"time"

	. "github.com/onsi/ginkgo/v2"
	. "github.com/onsi/gomega"
	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"
	"k8s.io/apimachinery/pkg/types"

	v1alpha1 "github.com/bluecontainer/petstore-operator/api/v1alpha1"
)

var _ = Describe("Order Controller Integration", func() {
	const (
		timeout  = time.Second * 10
		interval = time.Millisecond * 250

		resourceName      = "test-order"
		resourceNamespace = "default"
	)

	Context("When working with Order resources", func() {
		AfterEach(func() {
			// Clean up the resource after each test
			order := &v1alpha1.Order{}
			err := GetK8sClient().Get(GetContext(), types.NamespacedName{
				Name:      resourceName,
				Namespace: resourceNamespace,
			}, order)
			if err == nil {
				// Delete the resource
				_ = GetK8sClient().Delete(GetContext(), order)
				// Wait for deletion
				Eventually(func() bool {
					err := GetK8sClient().Get(GetContext(), types.NamespacedName{
						Name:      resourceName,
						Namespace: resourceNamespace,
					}, &v1alpha1.Order{})
					return err != nil
				}, timeout, interval).Should(BeTrue())
			}
		})

		It("Should create a Order resource successfully", func() {
			By("Creating a new Order")
			order := &v1alpha1.Order{
				TypeMeta: metav1.TypeMeta{
					APIVersion: "petstore.example.com/v1alpha1",
					Kind:       "Order",
				},
				ObjectMeta: metav1.ObjectMeta{
					Name:      resourceName,
					Namespace: resourceNamespace,
				},
				Spec: v1alpha1.OrderSpec{},
			}
			Expect(GetK8sClient().Create(GetContext(), order)).To(Succeed())

			By("Verifying the resource was created")
			created := &v1alpha1.Order{}
			Eventually(func() error {
				return GetK8sClient().Get(GetContext(), types.NamespacedName{
					Name:      resourceName,
					Namespace: resourceNamespace,
				}, created)
			}, timeout, interval).Should(Succeed())

			Expect(created.Name).To(Equal(resourceName))
			Expect(created.Namespace).To(Equal(resourceNamespace))
		})

		It("Should update a Order resource", func() {
			By("Creating a new Order")
			order := &v1alpha1.Order{
				ObjectMeta: metav1.ObjectMeta{
					Name:      resourceName,
					Namespace: resourceNamespace,
				},
				Spec: v1alpha1.OrderSpec{},
			}
			Expect(GetK8sClient().Create(GetContext(), order)).To(Succeed())

			By("Updating the Order")
			updated := &v1alpha1.Order{}
			Eventually(func() error {
				return GetK8sClient().Get(GetContext(), types.NamespacedName{
					Name:      resourceName,
					Namespace: resourceNamespace,
				}, updated)
			}, timeout, interval).Should(Succeed())

			// Update a field
			updated.Spec.TargetNamespace = "updated-namespace"
			Expect(GetK8sClient().Update(GetContext(), updated)).To(Succeed())

			By("Verifying the update")
			Eventually(func() string {
				fetched := &v1alpha1.Order{}
				_ = GetK8sClient().Get(GetContext(), types.NamespacedName{
					Name:      resourceName,
					Namespace: resourceNamespace,
				}, fetched)
				return fetched.Spec.TargetNamespace
			}, timeout, interval).Should(Equal("updated-namespace"))
		})

		It("Should delete a Order resource", func() {
			By("Creating a new Order")
			order := &v1alpha1.Order{
				ObjectMeta: metav1.ObjectMeta{
					Name:      resourceName,
					Namespace: resourceNamespace,
				},
				Spec: v1alpha1.OrderSpec{},
			}
			Expect(GetK8sClient().Create(GetContext(), order)).To(Succeed())

			By("Deleting the Order")
			toDelete := &v1alpha1.Order{}
			Expect(GetK8sClient().Get(GetContext(), types.NamespacedName{
				Name:      resourceName,
				Namespace: resourceNamespace,
			}, toDelete)).To(Succeed())
			Expect(GetK8sClient().Delete(GetContext(), toDelete)).To(Succeed())

			By("Verifying the resource is deleted")
			Eventually(func() bool {
				err := GetK8sClient().Get(GetContext(), types.NamespacedName{
					Name:      resourceName,
					Namespace: resourceNamespace,
				}, &v1alpha1.Order{})
				return err != nil
			}, timeout, interval).Should(BeTrue())
		})
	})
})
